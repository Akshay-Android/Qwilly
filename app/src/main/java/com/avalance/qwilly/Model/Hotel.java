public class Hotel {

    private String hotel_name;
    private String hotel_distance;
    private String hotel_address;
    private String hotel_imageUrl;
    private String hotel_rating;
    private String hotel_phone;
    private String hotel_id;

    public String getHotel_imageUrl() {
        return hotel_imageUrl;
    }

    public void setHotel_imageUrl(String hotel_imageUrl) {
        this.hotel_imageUrl = hotel_imageUrl;
    }

    public String getHotel_rating() {
        return hotel_rating;
    }

    public void setHotel_rating(String hotel_rating) {
        this.hotel_rating = hotel_rating;
    }

    public String getHotel_phone() {
        return hotel_phone;
    }

    public void setHotel_phone(String hotel_phone) {
        this.hotel_phone = hotel_phone;
    }

    public String getHotel_id() {
        return hotel_id;
    }

    public void setHotel_id(String hotel_id) {
        this.hotel_id = hotel_id;
    }

    public String getHotel_name() {
        return hotel_name;
    }

    public void setHotel_name(String hotel_name) {
        this.hotel_name = hotel_name;
    }

    public String getHotel_distance() {
        return hotel_distance;
    }

    public void setHotel_distance(String hotel_distance) {
        this.hotel_distance = hotel_distance;
    }

    public String getHotel_address() {
        return hotel_address;
    }

    public void setHotel_address(String hotel_address) {
        this.hotel_address = hotel_address;
    }




}
